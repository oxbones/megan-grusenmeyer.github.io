function triangles(num) {
  //create empty string
  var pound = ""; 
  //loop over pound
  for (var i = 0; pound.length < num; i++) {
    //redefine as concatenated with another pound 
    pound = pound.concat("#");
  //call to console each row
  console.log(pound);
  }
}

